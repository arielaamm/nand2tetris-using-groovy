class CompilationEngineException extends Exception {

    CompilationEngineException() {
    }

    CompilationEngineException(String message) {
        super(message);
    }

    CompilationEngineException(Throwable cause) {
        super(cause);
    }

    CompilationEngineException(String message, Throwable cause) {
        super(cause);
    }

}
